package org.eclipse.persistence.testing.models.aggregate;

public class Child {

    protected int id;
    protected Parent parent;

    public Child() {
        super();
    }

    public Child(int id) {
        this();
        this.id = id;
    }

    public Parent getParent() {
        return parent;
    }

    public void setParent(Parent parent) {
        this.parent = parent;
    }

    public int getId() {
        return this.id;
    }

    public void setId(int id) {
        this.id = id;
    }

}
