package org.eclipse.persistence.testing.models.aggregate;

public class Cousin {

    protected int id;
    protected String name;

    public Cousin() {
        super();
    }

    public Cousin(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
