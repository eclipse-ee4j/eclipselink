package org.eclipse.persistence.testing.models.jpa.composite.advanced.member_3;

import java.util.Objects;

public class PhoneNumberPK  {
    public Integer id;
    public String type;

    public PhoneNumberPK() {}

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    @Override
    public int hashCode() {
        return Objects.hash(id, type);
    }

    /**
     * equals: Answer true if the ids are equal
     */
    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        PhoneNumberPK that = (PhoneNumberPK) o;
        return Objects.equals(id, that.id);
    }

    @Override
    public String toString() {
        return "PhoneNumberPK [id=" + id + ", type=" + type + "]";
    }
}
